CREATE TABLE noticePlayers (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    notice_id INTEGER
        REFERENCES notices(id) ON DELETE CASCADE NOT NULL,
    name TEXT NOT NULL
);

CREATE TABLE noticeCharacters (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    notice_id INTEGER
        REFERENCES notices(id) ON DELETE CASCADE NOT NULL,
    name TEXT NOT NULL
);